CREATE PROCEDURE SP_Get_VoucherBelanja
	@Id bigint,
	@search nvarchar(max)
AS
BEGIN
	SET NOCOUNT ON;
	if(@Id = 0)
	begin
		select*from DataVoucher where NamaVoucher like '%'+@search+'%'
	end
	else
	begin
		select*from DataVoucher where id = @Id
	end
END

SET ANSI_NULLS ON

SET ANSI_NULLS ON
